impl<Flow, Request, Response> ConnectorCommonExt<Flow, Request, Response> for Elavon
where
    Self: ConnectorIntegration<Flow, Request, Response>,
{
    fn build_headers(
        &self,
        req: &types::RouterData<Flow, Request, Response>,
        _connectors: &settings::Connectors,
    ) -> CustomResult<Vec<(String, request::Maskable<String>)>, errors::ConnectorError> {
        let mut headers: Vec<(String, masking::Maskable<String>)> = vec![
            (
                headers::CONTENT_TYPE.to_string(),
                self.get_content_type().to_string().into(),
            ),
            (headers::ACCEPT_VERSION.to_string(), "1".to_string().into()),
            (
                headers::ACCEPT.to_string(),
                "application/json;charset=UTF-8".to_string().into(),
            ),
        ];
        let mut api_key = self.get_auth_header(&req.connector_auth_type)?;
        headers.append(&mut api_key);
        Ok(headers)
    }
}

impl ConnectorCommon for Elavon {
    fn id(&self) -> &'static str {
        "elavon"
    }

    fn get_currency_unit(&self) -> api::CurrencyUnit {
        api::CurrencyUnit::Base
    }

    fn common_get_content_type(&self) -> &'static str {
        "application/json;charset=UTF-8"
    }

    fn base_url<'a>(&self, connectors: &'a settings::Connectors) -> &'a str {
        connectors.elavon.base_url.as_ref()
    }

    fn get_auth_header(
        &self,
        auth_type: &types::ConnectorAuthType,
    ) -> CustomResult<Vec<(String, request::Maskable<String>)>, errors::ConnectorError> {
        let auth = elavon::ElavonAuthType::try_from(auth_type)
            .change_context(errors::ConnectorError::FailedToObtainAuthType)?;
        Ok(vec![(
            headers::AUTHORIZATION.to_string(),
            auth.api_key.expose().into_masked(),
        )])
    }

    fn build_error_response(
        &self,
        res: Response,
    ) -> CustomResult<ErrorResponse, errors::ConnectorError> {
        let response: elavon::ElavonErrorResponse = res
            .response
            .parse_struct("ElavonErrorResponse")
            .change_context(errors::ConnectorError::ResponseDeserializationFailed)?;

        let description = response
            .failures
            .iter()
            .map(|error| error.description.clone())
            .collect::<Vec<String>>()
            .join(" & ");

        Ok(ErrorResponse {
            status_code: res.status_code,
            code: match response.failures.get(0) {
                Some(failure_data) => failure_data.code.clone(),
                None => consts::NO_ERROR_CODE.to_string(),
            },
            message: description.clone(),
            reason: Some(description),
        })
    }
}

